int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	while (i < n)
	{
		j = s1[i] - s2[i];
		if (j != 0)
		{
			return (j);
		}
		i++;
	}
	return (0);
}
