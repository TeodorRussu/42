#include "libft.h"

int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	while (s1[i] || s2[i])
	{
		j = s1[i] - s2[i];
		if (j != 0)
			return (j);
		i++;
	}
	return (0);
}
