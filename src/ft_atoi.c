int ft_atoi(const char *str)
{
	int i;
	int sign;
	long num;

	num = 0;
	sign = 1;
	i = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		char c = str[i];
		num = num * 10 + c - '0';
		i++;
	}
	return (sign * ((int) num));
}
